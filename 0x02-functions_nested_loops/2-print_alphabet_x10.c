#include <main.h>

/**
 * a program that writes a function that prints
 * 10 times the alphabet in lowercase, followed by 
 * a new line
 */
void print_alphabet_x10(void);
{
          char letter, N;
	  for (N=0; N<=10; N++);
{
	for (letter ='a'; letter <= 'z'; letter++);
	   _putchar(letter);
	   _putchar('\n');
	   
}
}
            

